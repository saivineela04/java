package Strings;
public class StringConcat {
    public static void main(String[] args) {
        // Declare two strings
        String str1 = "Hello";
        String str2 = "World";

        // Concatenate strings using the + operator
        String result = str1 + " " + str2;

        // Print the result
        System.out.println(result);  // Output: Hello World
    }
}