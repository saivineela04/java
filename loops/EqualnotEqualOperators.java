package loops;
public class EqualnotEqualOperators {
    public static void main(String[] args) {
        int a = 5, b = 10;

        // Equal operator
        if (a == b) {
            System.out.println("a is equal to b");
        } else {
            System.out.println("a is not equal to b");
        }

        // Not equal operator
        if (a != b) {
            System.out.println("a is not equal to b");
        }
    }
}